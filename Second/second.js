// Definición de variables
const a = 4;
const b = 7;
const c = 2 * b;
const x = a;
const y = 3 * x;
const u = 6;
const w = 10;

// Operación a
const resultA = a / (b * c);

// Operación b
const resultB = Math.pow(a, 2) + Math.pow(b, 92);

// Operación c
const resultC = (x + y) / (u + w / a);

// Operación d
const z = resultC; // Tomamos el resultado de la operación c ya que z no tiene valor.
const resultD = (x / y) * (z + w);

// Mostrar resultados en la consola
console.log("Resultado de a:", resultA);
console.log("Resultado de b:", resultB);
console.log("Resultado de c:", resultC);
console.log("Resultado de d:", resultD);
